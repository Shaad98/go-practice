package main

import (
	"fmt"
	"math/rand"
)

func main() {

	fmt.Println("################## Loops Practice ##################")

	// ==========================================================
	// 1 :: Infinite Loop
	// ==========================================================

	/*
		for {
			fmt.Println("Hello World")
		}

		for true {
			fmt.Println("Hello World")
		}
	*/

	// ==========================================================
	// 2 :: Traditional For Loop
	// ==========================================================

	/*
		for i := 1; i <= 5; i++ {
			fmt.Println(i)
		}
	*/

	// ==========================================================
	// 3 :: While Loop (Go Style)
	// ==========================================================

	/*
		i := 1

		for i <= 5 {
			fmt.Println(i)
			i++
		}
	*/

	// ==========================================================
	// 4 :: Reverse Loop
	// ==========================================================

	/*
		for i := 10; i >= 1; i-- {
			fmt.Println(i)
		}
	*/

	// ==========================================================
	// 5 :: Increment by 2
	// ==========================================================

	/*
		for i := 0; i <= 20; i += 2 {
			fmt.Println(i)
		}
	*/

	// ==========================================================
	// 6 :: Multiple Variables
	// ==========================================================

	/*
		for i, j := 1, 10; i <= 5; i, j = i+1, j-1 {
			fmt.Println(i, j)
		}
	*/

	// ==========================================================
	// 7 :: Nested Loops
	// ==========================================================

	/*
		for i := 1; i <= 3; i++ {

			for j := 1; j <= 3; j++ {
				fmt.Printf("i = %d, j = %d\n", i, j)
			}

		}
	*/

	// ==========================================================
	// 8 :: break
	// ==========================================================

	/*
		for i := 1; i <= 10; i++ {

			if i == 5 {
				break
			}

			fmt.Println(i)
		}
	*/

	// ==========================================================
	// 9 :: continue
	// ==========================================================

	/*
		for i := 1; i <= 5; i++ {

			if i == 3 {
				continue
			}

			fmt.Println(i)
		}
	*/

	// ==========================================================
	// 10 :: Loop through String
	// ==========================================================

	/*
		name := "Shaad"

		for i := 0; i < len(name); i++ {
			fmt.Println(string(name[i]))
		}
	*/

	// ==========================================================
	// 11 :: range with Slice
	// ==========================================================

	/*
		nums := []int{10, 20, 30, 40}

		for index, value := range nums {
			fmt.Println(index, value)
		}
	*/

	// ==========================================================
	// 12 :: Ignore Index using _
	// ==========================================================

	/*
		nums := []int{10, 20, 30}

		for _, value := range nums {
			fmt.Println(value)
		}
	*/

	// ==========================================================
	// 13 :: Ignore Value
	// ==========================================================

	/*
		nums := []int{10, 20, 30}

		for index := range nums {
			fmt.Println(index)
		}
	*/

	// ==========================================================
	// 14 :: Infinite Loop with break
	// ==========================================================

	/*
		i := 1

		for {

			fmt.Println(i)

			if i == 5 {
				break
			}

			i++
		}
	*/

	// ==========================================================
	// 15 :: Label with break
	// ==========================================================

	/*
	Outer:
		for i := 1; i <= 3; i++ {

			for j := 1; j <= 3; j++ {

				if i == 2 && j == 2 {
					break Outer
				}

				fmt.Println(i, j)
			}
		}
	*/

	// ==========================================================
	// 16 :: Random Number with Loop
	// ==========================================================

	/*
		for i := 1; i <= 5; i++ {
			fmt.Println(rand.Intn(100))
		}
	*/

	// ==========================================================
	// 17 :: Empty Initialization
	// ==========================================================

	/*
		i := 1

		for ; i <= 5; i++ {
			fmt.Println(i)
		}
	*/

	// ==========================================================
	// 18 :: Empty Increment
	// ==========================================================

	/*
		i := 1

		for i <= 5 {

			fmt.Println(i)

			i++
		}
	*/

	// ==========================================================
	// 19 :: Empty Condition (Infinite until break)
	// ==========================================================

	/*
		i := 1

		for ; ; {

			fmt.Println(i)

			if i == 5 {
				break
			}

			i++
		}
	*/

	// ==========================================================
	// 20 :: Input Until User Enters 0
	// ==========================================================

	/*
		for {

			var num int

			fmt.Print("Enter Number (0 to Exit): ")
			fmt.Scan(&num)

			if num == 0 {
				break
			}

			fmt.Println("You Entered:", num)
		}
	*/

	// Prevent unused import error for math/rand
	_ = rand.Intn
}